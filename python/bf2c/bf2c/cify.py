    def _get_line(_instruct, depth = 1):
        match _instruct._type:
            case "data_right":
                return f"p+={_instruct.arg};"
            case "data_left":
                return f"p-={_instruct.arg};"
            case "inc_data":
                return f"t[p]+={_instruct.arg};"
            case "dec_data":
                return f"t[p]-={_instruct.arg};"
            case "write":
                return "putchar(t[p]);" * _instruct.arg
            case "read":
                return "t[p]=getchar();" * _instruct.arg
            case "loop":
                _lines = []
                for _inst in _instruct.arg:
                    _lines.append(_get_line(_inst, depth=depth + 1))

                _lines = "".join(_lines)
                return "while(t[p]!=0){" + _lines + "}"

    for instruction in instructions:
        lines.append(_get_line(instruction))

    lines = "".join(lines)

