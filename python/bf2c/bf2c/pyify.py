from parser import Instruction, parse


def pyify(instructions: list[Instruction]):
    lines = []

    def _get_line(_instruct, depth = 1):
        match _instruct._type:
            case "data_right":
                return f"position += {_instruct.arg}"
            case "data_left":
                return f"position -= {_instruct.arg}"
            case "inc_data":
                return f"tape[position] += {_instruct.arg}"
            case "dec_data":
                return f"tape[position] -= {_instruct.arg}"
            case "write":
                indent = "    " * depth
                writes = ["print(chr(tape[position]), end=\"\")"] * _instruct.arg
                return f"\n{indent}".join(writes)
            case "read":
                print("no reading")
                exit(-1)
            case "loop":
                _lines = []
                for _inst in _instruct.arg:
                    _lines.append(_get_line(_inst, depth=depth + 1))

                indent = "    " * (depth + 1)
                _lines = f"\n{indent}".join(_lines)

                #print(_lines)

                return f"while tape[position] != 0:\n{indent}" + _lines

    for instruction in instructions:
        lines.append(_get_line(instruction))

    lines = "\n    ".join(lines)
    #print(lines)

    return f"""
def main():
    tape = [0] * 30_000
    position = 0
    {lines}

main()
"""


