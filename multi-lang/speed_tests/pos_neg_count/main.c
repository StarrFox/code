// we can use char because all our numbers are between -100 and 100
static unsigned char max_count(signed char* input, int size) {
    // these two need to be at least big enough to store <size>
    int positive = 0, negative = 0;

    // same for index
    for (int index = 0; index < size; ++index) {
        signed char test = input[index];

        if (test == 0) {
            continue;
        }
        else if (test > 0) {
            ++positive;
        }
        else {
            ++negative;
        }
    }

    if (positive > negative) {
        return positive;
    }

    return negative;
}

